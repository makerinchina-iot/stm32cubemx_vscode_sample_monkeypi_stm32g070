void fan_click_cb(sgl_event_t *e)
{
    sgl_obj_t *fan_icon_r = (sgl_obj_t *)(e->param);
    if (e->type == SGL_EVENT_PRESSED)
    {
        // log_info("sgl event press");
        sgl_rect_set_border_color(fan_r, CLR_WHITE);
        sgl_icon_set_color(fan_icon_r, CLR_CYBER_BLUE);
    }
    else if (e->type == SGL_EVENT_RELEASED)
    {
        // log_info("sgl event release");

        g_fan_state = !g_fan_state;

        if (g_fan_state)
        {
            sgl_rect_set_border_color(fan_r, CLR_CYBER_BLUE);
            sgl_icon_set_color(fan_icon_r, CLR_CYBER_BLUE);

            // fan is running
            sgl_anim_start(fan_anim, SGL_ANIM_REPEAT_LOOP);
        }
        else
        {
            sgl_rect_set_border_color(fan_r, CLR_DIVIDER);
            sgl_icon_set_color(fan_icon_r, CLR_TEXT_GRAY);

            sgl_anim_stop(fan_anim);
        }

        log_info("fan state : %d", g_fan_state);
    }
}

