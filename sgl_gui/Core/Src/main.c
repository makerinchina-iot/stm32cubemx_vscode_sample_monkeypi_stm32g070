void tft_panel_flush_area(sgl_area_t *area, sgl_color_t *src)
{

    int16_t x1 = area->x1;
    int16_t y1 = area->y1;
    int16_t x2 = area->x2;
    int16_t y2 = area->y2;

    const uint16_t w = x2 - x1 + 1;
    const uint16_t h = y2 - y1 + 1;

    // tft flush win
    // log_debug("draw pos: %d, %d, %d, %d", x1, y1, x2, y2);

    st7789_fill_area(x1, y1, w, h, (uint16_t *)src);

    sgl_fbdev_flush_ready();
}

